# Escape Sequences

# "\t"  Tab
ex_1 = "This\tis\ta\tlot\tof\tspace..." 
print(ex_1)

# "\n"  New Line
ex_2 = "line one\nline two"
print(ex_2)

# "\'"  Single Quote
# "\""  Double Quote
ex_3 = '"When I said \'immediately\' I meant today!" said King Saul'
ex_4 = "\"Do or do not. There is no try.\""
print(ex_3)
print(ex_4)

# "\\"  Backslash
ex_5 = "All scape sequences contais a \\"
print(ex_5)

