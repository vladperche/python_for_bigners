# Type

ex_1 = False
ex_2 = 29
ex_3 = 4.1352

print(type(ex_1))   #<class 'bool'>
print(type(ex_2))   #<class 'int'>
print(type(ex_3))   #<class 'float'>

# Str

ex_4 = str(True)
ex_5 = str(5)
ex_6 = str(3.21)

print(type(ex_4))   #<class 'str'>
print(type(ex_5))   #<class 'str'>
print(type(ex_6))   #<class 'str'>
