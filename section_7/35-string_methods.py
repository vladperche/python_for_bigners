# Upper / Lower

all_low = "there are no capital here."
print(all_low.upper())
print(all_low)

all_up = "THERE IS SHOUTING TEXT!"
print(all_up.lower())
print(all_up)

# isupper / islower

print("Mixed Case".isupper())
print("ALL CAPS!".isupper())

print("Mixed Case".islower())
print("$100 per hour".islower())

