# Range returns a sequence of numbers

# Example 1 ==================================

print("Example 1")
one_input = range(5)

for num in one_input:
    print(num)

# Example 2 ==================================

print("Example 2")
two_inputs = range(11,16)

for num in two_inputs:
    print(num)

# Example 3 ==================================

print("Example 3")
three_inputs = range(10, 25, 5)

for num in three_inputs:
    print(num)

# Example 4 ==================================

print("Example 4")

downgrade_sequence = range(20, 1, -3)
for num in downgrade_sequence:
    print(num)

