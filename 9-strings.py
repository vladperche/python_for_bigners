# Strings
# Strings are combinations of characters

ex_1 = 'This is a string'
ex_2 = "This is also a string"

print(ex_1)
print(ex_2)

# Accessing by index (zero-based)

ex_8 = "orange"

print(ex_8[2]) # a
print("apple"[4]) # e

# Slicing

ex_10 = "apricots"

print(ex_10[:3])
print(ex_10[2:5])
print(ex_10[4:])

# Concatenation

ex_11 = "pecan"
ex_12 = "pie"
ex_13 = ex_11 + " " + ex_12

print(ex_13)
